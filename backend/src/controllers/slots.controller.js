/**
 * src/controllers/slots.controller.js
 * Manage time slot availability per salon per date
 */

import Slot from '../models/Slot.js';
import Salon from '../models/Salon.js';

// Helper: generate 30-minute time slots between two times
function generateTimeSlots(openTime = '09:00', closeTime = '21:00') {
    const slots = [];
    let [h, m] = openTime.split(':').map(Number);
    const [endH, endM] = closeTime.split(':').map(Number);
    const endMinutes = endH * 60 + endM - 60; // stop 1h before close

    while (h * 60 + m <= endMinutes) {
        slots.push(`${String(h).padStart(2, '0')}:${String(m).padStart(2, '0')}`);
        m += 30;
        if (m >= 60) { m = 0; h++; }
    }
    return slots;
}

// ── GET /api/slots/:salonId?date=YYYY-MM-DD ──────────────
// Returns all slots for a salon on a given date.
// If no slots have been added by admin for that date, auto-generates from open/close times.
export async function getSlots(req, res) {
    try {
        const { salonId } = req.params;
        const { date } = req.query;

        if (!date) {
            return res.status(400).json({ error: 'Query param ?date=YYYY-MM-DD is required.' });
        }
        if (!/^\d{4}-\d{2}-\d{2}$/.test(date)) {
            return res.status(400).json({ error: 'Date must be in YYYY-MM-DD format.' });
        }

        // Validate salon exists
        const salon = await Salon.findById(salonId).select('openTime closeTime');
        if (!salon) {
            return res.status(404).json({ error: 'Salon not found.' });
        }

        // Get all slots for this salon + date from DB
        let slots = await Slot.find({ salonId, date }).lean();

        // If no admin-defined slots, auto-generate from salon hours
        if (slots.length === 0) {
            const times = generateTimeSlots(salon.openTime, salon.closeTime);
            slots = times.map(time => ({
                salonId,
                date,
                time,
                isBooked: false,
                bookingId: null,
                _autoGenerated: true,
            }));
        }

        res.json({
            salonId,
            date,
            slots: slots.map(s => ({
                time: s.time,
                isBooked: s.isBooked,
                bookingId: s.bookingId || null,
            })),
            total: slots.length,
            available: slots.filter(s => !s.isBooked).length,
        });
    } catch (err) {
        if (err.name === 'CastError') {
            return res.status(400).json({ error: 'Invalid salon ID.' });
        }
        console.error('getSlots error:', err);
        res.status(500).json({ error: 'Failed to fetch slots.' });
    }
}

// ── POST /api/slots/:salonId (admin only) ────────────────
// Admin can pre-define available slots for a date.
// Body: { date: "YYYY-MM-DD", times: ["09:00", "09:30", ...] }
export async function addSlots(req, res) {
    try {
        const { salonId } = req.params;
        const { date, times } = req.body;

        if (!date || !Array.isArray(times) || times.length === 0) {
            return res.status(400).json({
                error: 'date (YYYY-MM-DD) and times[] array are required.',
            });
        }

        // Validate each time
        const invalidTime = times.find(t => !/^\d{2}:\d{2}$/.test(t));
        if (invalidTime) {
            return res.status(400).json({
                error: `Invalid time format: "${invalidTime}". Must be HH:MM.`,
            });
        }

        // Upsert slots (insert if not exists, skip if exists)
        const ops = times.map(time => ({
            updateOne: {
                filter: { salonId, date, time },
                update: { $setOnInsert: { salonId, date, time, isBooked: false } },
                upsert: true,
            },
        }));
        await Slot.bulkWrite(ops);

        // Return the updated slot list
        const slots = await Slot.find({ salonId, date }).lean();

        res.status(201).json({
            message: `${times.length} slot(s) added for ${date}.`,
            slots: slots.map(s => ({
                time: s.time,
                isBooked: s.isBooked,
            })),
        });
    } catch (err) {
        console.error('addSlots error:', err);
        res.status(500).json({ error: 'Failed to add slots.' });
    }
}

// ── DELETE /api/slots/:salonId?date=YYYY-MM-DD (admin) ───
// Remove all un-booked slots for a specific date
export async function removeSlots(req, res) {
    try {
        const { salonId } = req.params;
        const { date } = req.query;
        if (!date) return res.status(400).json({ error: 'date query param required.' });

        const result = await Slot.deleteMany({ salonId, date, isBooked: false });
        res.json({ message: `${result.deletedCount} slot(s) removed for ${date}.` });
    } catch (err) {
        console.error('removeSlots error:', err);
        res.status(500).json({ error: 'Failed to remove slots.' });
    }
}
